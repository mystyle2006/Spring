package com.java.ex;

public class Calculator {
	public void add(int f, int s) {
		System.out.println(f + " + " + s + " = " + (f + s));
	}
	
	public void sub(int f, int s) {
		System.out.println(f + " + " + s + " = " + (f - s));
	}
	
	public void multi(int f, int s) {
		System.out.println(f + " + " + s + " = " + (f * s));
	}
	
	public void div(int f, int s) {
		System.out.println(f + " + " + s + " = " + (f / s));
	}
}
